public class J2_3_1 {
  public static void main (String[] args) {
    int a1 = 5, b1 = 3;
    System.out.println(a1 + "/" + b1 + " = " + a1 / b1); 
    float a2 = 5.0f, b2 = 3.0f;
    System.out.println(a2 + "/" + b2 + " = " + a2 / b2); 
    double a3 = 5.0, b3 = 3.0;
    System.out.println(a3 + "/" + b3 + " = " + a3 / b3); 
    System.out.println("3" + 5 * 4); 
  }
}